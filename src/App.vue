            this.curSelComps[0].addTransformer()
            this.cancelSelGroup()
          } else {
            this.addToGroup()
          }
        } else {
          // console.log('ungroup')
          this.konvaObjs.transformer.detach()
          this.cancelSelGroup()
        }
        this.syncKonvaZIndex()
      },
      curSelCompLayout: {
